#!/usr/bin/env node
// Builds MUTATION-AUDIT.md from a completed Stryker run, for the weekly
// scheduled audit (.github/workflows/mutation-audit.yml).
//
// WHY the JSON reporter for per-mutant detail: the clear-text table only has
// per-file percentages, not per-mutant file/line/mutator information. Requires
// "json" in stryker.config.mjs reporters (reports/mutation/mutation.json).
//
// WHY the overall score still comes from the clear-text log, not computed here
// from the JSON mutant counts: the global pre-push hook's ratchet
// (~/.claude/config/git-hooks/pre-push, run_stryker) already has a proven parser
// for the clear-text "All files" row, and Stryker's own score formula has
// nonobvious edge cases around Ignored/static mutants -- reusing the same log
// line avoids re-deriving that formula a second, possibly-inconsistent way.
//
// Usage: npx stryker run --incremental 2>&1 | tee stryker-output.log
//        node scripts/mutation-audit-summary.mjs stryker-output.log

import { readFileSync, existsSync, writeFileSync } from "node:fs";
import strykerConfig from "../stryker.config.mjs";

const LOG_FILE = process.argv[2] || "stryker-output.log";
const JSON_REPORT = "reports/mutation/mutation.json";
const OUTPUT_FILE = "MUTATION-AUDIT.md";
const MAX_LISTED = 40;

if (!existsSync(LOG_FILE)) {
  console.error(
    `${LOG_FILE} not found. Run "npx stryker run --incremental 2>&1 | tee ${LOG_FILE}" first.`,
  );
  process.exit(1);
}
if (!existsSync(JSON_REPORT)) {
  console.error(
    `${JSON_REPORT} not found. Add "json" to reporters in stryker.config.mjs.`,
  );
  process.exit(1);
}

const log = readFileSync(LOG_FILE, "utf8");
const scoreLine = log.split("\n").find((line) => line.startsWith("All files"));
if (!scoreLine) {
  console.error(
    'Could not find an "All files" row in the clear-text log. Check that the ' +
      "clear-text reporter is enabled in stryker.config.mjs.",
  );
  process.exit(1);
}
const score = scoreLine.split("|")[1]?.trim();
if (!/^\d+(\.\d+)?$/.test(score ?? "")) {
  console.error(
    `Could not parse a numeric score from the "All files" row: "${scoreLine}"`,
  );
  process.exit(1);
}

const report = JSON.parse(readFileSync(JSON_REPORT, "utf8"));
const { high, low } = strykerConfig.thresholds;
const numericScore = Number(score);
const band =
  numericScore >= high ? "healthy" : numericScore >= low ? "warning" : "danger";

// Survived: a test exercised this code but no assertion caught the mutation.
// NoCoverage: no test even executed this code path at all. Both are real gaps;
// Killed/Ignored/Timeout/CompileError/RuntimeError are not actionable findings.
const findings = [];
for (const [file, fileResult] of Object.entries(report.files)) {
  for (const mutant of fileResult.mutants) {
    if (mutant.status === "Survived" || mutant.status === "NoCoverage") {
      findings.push({
        file,
        line: mutant.location.start.line,
        mutator: mutant.mutatorName,
        status: mutant.status,
      });
    }
  }
}
findings.sort((a, b) => a.file.localeCompare(b.file) || a.line - b.line);

const date = new Date().toISOString().slice(0, 10);
const lines = [];
lines.push("# Mutation Audit", "");
lines.push(`**Date:** ${date}`);
lines.push(
  `**Score:** ${score}% (${band}, thresholds: high ${high}%, low ${low}%)`,
);
lines.push(`**Surviving + uncovered mutants:** ${findings.length}`, "");
lines.push(
  "This file is regenerated by the weekly `.github/workflows/mutation-audit.yml` run. " +
    "It is informational only: mutation testing finds weak or missing assertions, but a " +
    "human still has to judge each finding, since some surviving mutants are equivalent " +
    "mutants with no real behavior difference rather than real gaps, before writing a " +
    "test. Nothing here auto-merges.",
  "",
);

if (findings.length === 0) {
  lines.push("No surviving or uncovered mutants this run.");
} else {
  lines.push(`## Top ${Math.min(MAX_LISTED, findings.length)} findings`, "");
  lines.push("| File | Line | Mutator | Status |");
  lines.push("|---|---|---|---|");
  for (const f of findings.slice(0, MAX_LISTED)) {
    lines.push(`| \`${f.file}\` | ${f.line} | ${f.mutator} | ${f.status} |`);
  }
  if (findings.length > MAX_LISTED) {
    lines.push(
      "",
      `...and ${findings.length - MAX_LISTED} more. Full detail is in the HTML report artifact attached to the workflow run.`,
    );
  }
}

lines.push(
  "",
  "Full interactive report: download the `mutation-report` artifact from the workflow run that opened this PR.",
);

writeFileSync(OUTPUT_FILE, lines.join("\n") + "\n");
console.log(
  `Wrote ${OUTPUT_FILE}: score ${score}%, ${findings.length} findings.`,
);
